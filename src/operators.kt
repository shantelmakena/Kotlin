fun main() {
    //Arithmetic Operators
    var x = 90
    var y = 5
    println(x + y )
    println(x - y )
    println(x * y )
    println(x / y )
    println(x % y )  //Modulus Operator


    //Comparison Operators
    var num1 = 45
    var num2 = 30
    println(num1 < num2)
    println(num1 > num2)
    println(num1 <= num2)
    println(num1 >= num2)
    println(num1 == num2)
    println(num1 != num2)

    //Assignment Operators
    var number = 10
    println(number)

    number += 2
    println(number)


    //Logic Operators - &&(and), ||(or), !(not)
    println((100 > 20) && (80 < 10))
    println((100 > 20) || (80 < 10))
    println(!((100 > 20) || (80 < 10)))


}